// Exercício 1 - Conjunto de funções matemáticas no file e corer utilizando o Node.js.

// Declaração das funções
function som(a, b) { return a + b }
function sub(a, b) { return a - b }
function mul(a, b) { return a * b }
function div(a, b) { return a / b }
function mod(a, b) { return a % b }

// Execução com print dos resultados
console.log('Sum » 201 + 100 =', som(201, 100))
console.log('Sub » 201 - 100 =', sub(201, 100))
console.log('Mul » 201 * 100 =', mul(201, 100))
console.log('Div » 201 / 100 =', div(201, 100))
console.log('Mod » 201 % 100 =', mod(201, 100))